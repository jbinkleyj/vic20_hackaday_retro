; ENC28J60 SPI Ethernet adapter driver
; SPI commands and device control registers

; Low-level SPI commands (aaaaa = control register address)
E28_RCR=%00000000	; 000aaaaa = Read Control Register
E28_RBM=%00111010	; Read Buffer Memory
E28_WCR=%01000000	; 010aaaaa + data = Write Control Register
E28_WBM=%01111010	; 01111010 + data = Write Buffer Memory
E28_BFS=%10000000	; 100aaaaa + data = Bit Field Set
E28_BFC=%10100000	; 101aaaaa + data = Bit Field Clear
E28_SRC=%11111111	; 11111111 = Soft Reset Command

; Buffer pointers
